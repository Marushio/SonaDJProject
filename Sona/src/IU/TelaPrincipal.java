    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        IControlePrincipal controlePrincipal = NegocioFactory.obterControleLogin();
        String login = tfLogin.getText();
        String senha = pfSenha.getText();
        boolean status = controlePrincipal.cadastrar(login, senha);
        if(status)
            lbStatus.setText("Usu치rio cadastrado com sucesso");
        else
            lbStatus.setText("Usu치rio j치 existente, tente outro.... :(");
    }//GEN-LAST:event_btCadastrarActionPerformed

    private void btLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLogarActionPerformed
        // TODO add your handling code here:
        IControlePrincipal controlePrincipal = NegocioFactory.obterControleLogin();
        String login = tfLogin.getText();
        String senha = pfSenha.getText();
        Usuario usuario = controlePrincipal.logar(login, senha);
        if(usuario == null){
            lbStatus.setText("Login ou senha incorretos");
        }else{
            TelaPlayList tp = new TelaPlayList(usuario);
            tp.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btLogarActionPerformed

